print("Unformatted")
print(f"{123.456789}")
print()
print("Formatted as decimals")
print(f"{123.456789:8.2f}")
print(f"{123:8.2f}")
print(f"{123456.789:8.2f}")
print()
print("Formatted in scientific notation")
print(f"{123.456789:8.2e}")
print(f"{123:8.2e}")
print(f"{123456.789:8.2e}")
print(f"{0.00123456789:8.2e}")
print()
print("Formatted as currency")
print(f"£{0.123456789:.2f}")
print(f"£{123.456789:.2f}")
print(f"£{123456.789:.2f}")
print()
print("Formatted as percentages")
print(f"{0.123456789:9.2%}")
print(f"{123:9.2%}")

